            <p>Kapling (Inbreeding Probability Calculator) is an application to calculate the probability of inbreeding in monoparous animals (single birth type).</p>
            <p>The calculation of inbreeding probability is based on the following assumptions:</p>
            <p>1. The calculation is done in the first generation.</p>
            <p>2. Service per Conception in the AI (Artificial Insemination) system is 1.00.</p>
            <p>3. Natural mating in parents and between the first generation occurs randomly.</p>
            <p>4. Artificial insemination in parents is non-random, but in the first generation, it occurs randomly.</p>
            <p>5. In the AI system, the number of females is equal to the number of frozen semen.</p>
            <p>6. Each natural mating and AI produces 1 offspring.</p>
